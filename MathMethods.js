/* 
Math Methods in JavaScript
    -round()
    -floor()
    -ceil()
    -pow()
    -sqrt()
    -min()
    -max()
    -PI()
*/

var m_round,m_floor,m_ceil,m_pow,m_sqrt,m_min,m_max,m_pi;

//Round
m_round=Math.round(3.6);
console.log(m_round);

//floor
m_floor=Math.floor(3.9);
console.log(m_floor);

//ceil
m_ceil=Math.ceil(3.1);
console.log(m_ceil);

//pow
m_pow=Math.pow(3,3);
console.log(m_pow);


//SQRT
m_sqrt=Math.sqrt(9);
console.log(m_sqrt)

//MIN
m_min=Math.min(3,2,4,5,6,7);
console.log(m_min);

//Max
m_max=Math.max(3,2,4,5,6,7);
console.log(m_max);

//PI
m_pi=Math.PI;
console.log(m_pi);